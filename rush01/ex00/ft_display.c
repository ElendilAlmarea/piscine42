#include "rush01.h"

void	ft_display(int **tab, int size)
{
	int		x;
	int		y;
	char	c;

	x = 1;
	while (x < size - 1)
	{
		y = 1;
		while (y < size - 1)
		{
			c = tab[x][y] + '0';
			write(1, &c, 1);
			write(1, " ", 1);
			y++;
		}
		x++;
		write(1, "\n", 1);
	}
}
