#include <unistd.h>

void	ft_print_comb(void)
{
	int	nb1;
	int	nb2;
	int	nb3;

	nb1 = 47;
	while (nb1++ < 57)
	{
		nb2 = 48;
		while (nb2++ < 57)
		{
			nb3 = 49;
			while (nb3++ < 57)
			{
				if ((nb1 < nb2 && nb2 < nb3) && (nb1 + nb2 + nb3) != 168)
				{
					write(1, &nb1, 1);
					write(1, &nb2, 1);
					write(1, &nb3, 1);
					write(1, ", ", 2);
				}
			}
		}
	}
	write(1, "789", 3);
}
