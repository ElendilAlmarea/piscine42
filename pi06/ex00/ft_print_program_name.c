#include <unistd.h>

int	main(int argc, char *argv[])
{
	char	*str;
	int		i;
	int		useless;

	useless = argc;
	str = argv[0];
	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
