#include "bsq.h"

void		init_var(t_var *var)
{
	var->mid = 1;
	var->k1 = 0;
	var->k2 = 0;
	var->m = 0;
	var->n = 0;
	var->d = 2147483647;
	var->dl = 2147483647;
	var->dr = 2147483647;
}

void		init_t(t_var *v, t_table *t)
{
	t->lc++;
	v->ls = 0;
	v->i = 0;
	v->s1 = 0;
	v->s2 = 0;
}
